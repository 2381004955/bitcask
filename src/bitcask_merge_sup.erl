-module(bitcask_merge_sup).

-behaviour(supervisor).

%% API
-export([start_link/0]).

%% Supervisor callbacks
-export([init/1]).


%% ===================================================================
%% API functions
%% ===================================================================

start_link() ->
    supervisor:start_link({local, ?MODULE}, ?MODULE, []).


%% ===================================================================
%% Supervisor callbacks
%% ===================================================================

init([]) ->
    Workers = worker_specs(bitcask_app:get_env(merge_workers), []),
    {ok,{{one_for_one,1,1}, Workers}}.



%% ===================================================================
%% Internal functions
%% ===================================================================

worker_specs(0, Acc) ->
    Acc;
worker_specs(Count, Acc) ->
    Id = list_to_atom(lists:concat(['bitcask_merge_worker_', Count])),
    Spec = {Id, {bitcask_merge_worker, start_link, []},
            permanent, 5000, worker, [bitcask_merge_worker]},
    worker_specs(Count-1, [Spec | Acc]).
